function ShinyBobGFX_settings()
    if settings.startup['replace-circuits'].value then
        tableMerge(colors,ShinyBobGFX_circuits)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_circuits_filters)
    end
    if settings.startup['replace-assemblycolors'].value then
        tableMerge(colors,ShinyBobGFX_assembly)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_assembly_filters)
    end
    if settings.startup['new-furnaces'].value then
        tableMerge(colors,ShinyBobGFX_furnaces)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_furnaces_filters)
    end
    if settings.startup['combat-icons'].value then
        tableMerge(colors,ShinyBobGFX_combat)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_combat_filters)
    end
    if settings.startup['fancy-inserters'].value then
        tableMerge(colors,ShinyBobGFX_inserters)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_inserters_filters)
    end
    if settings.startup['add-powerbars'].value and not settings.startup['replace-assemblycolors'].value then
        tableMerge(colors,ShinyBobGFX_powerbars)
        listMerge(ShinyBobGFX_filters,ShinyBobGFX_powerbars_filters)
    end
end